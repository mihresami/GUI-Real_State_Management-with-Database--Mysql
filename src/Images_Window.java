    private void RomoveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RomoveBtnActionPerformed
        // Remove the image
       try{
         String selectedItem = String.valueOf(jList1.getSelectedValue());
        Integer imageId = Integer.valueOf(selectedItem);
        int Yes_No = JOptionPane.showConfirmDialog(null, "Do you want to Delete this Image", "Delete Type", JOptionPane.YES_NO_OPTION);
        if(Yes_No ==JOptionPane.YES_OPTION){
        if(new Property().removeImage(imageId)){
            JOptionPane.showMessageDialog(null, "This Image has been deleted","Delete Type",1);
            jLabel_Image.setIcon(null);
        }
        else{
            JOptionPane.showMessageDialog(null, "Operation Failed","Delete Type",0);
        }
        }
           
       }
       catch(Exception ex){
           JOptionPane.showMessageDialog(null, "NO Image Selected","Error",0);
       }

        
        
        

    }//GEN-LAST:event_RomoveBtnActionPerformed

