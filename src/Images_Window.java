      public void ShowPropertiesTable() {
        Property property = new Property();
        ArrayList<Property> list = property.PropertyList();
        //	Id. type square_feet ownerId price address bedrooms bathrooms age balcony pool backyard garage description
        //table columns
        String[] ColNames = {"ID", "Address"};
        Object[][] rows = new Object[list.size()][2];
        //add data form the list to the rows
        for (int i = 0; i < list.size(); i++) {
            rows[i][0] = list.get(i).getId();
            rows[i][1] = list.get(i).getAddress();
   
     

        }
        DefaultTableModel model = new DefaultTableModel(rows, ColNames);
        jTable1.setModel(model);
        

    }
       public void fillTypeList() {
        PropertyType type = new PropertyType();
        HashMap<byte[], Integer> imageList = new Property().PropertyImageList(propertyId);
        DefaultListModel listModel = new DefaultListModel();
        int i = 0;
        for (Integer id : imageList.values()) {
            listModel.add(i, id);
            i++;

        }
        jList1.setModel(listModel);

    }

